# Метод прогонки для решения системы линейных уравнений

# Коэффициенты системы
a = [0, 2, -8, -6, 9]  # нижняя диагональ
b = [-6, 10, 18, 17, 14]  # главная диагональ
c = [6, -7, 9, -6, 0]  # верхняя диагональ
d = [30, -31, 108, -114, 124]  # вектор правой части

# Количество уравнений
n = len(d)

# Прямой ход
for i in range(1, n):
    m = a[i] / b[i - 1]
    b[i] = b[i] - m * c[i - 1]
    d[i] = d[i] - m * d[i - 1]

# Обратный ход
x = [0] * n
x[-1] = d[-1] / b[-1]

for i in range(n - 2, -1, -1):
    x[i] = (d[i] - c[i] * x[i + 1]) / b[i]

# Печать решения
print("Решение системы методом прогонки:", x)
